package pkg02_Loops;

public class C22_WhileLoop {

    public static void main(String[] args) {

        // while loop
        int index =90;
        while (index < 15)
        {
            System.out.print(index + " ");
            index++;
        }









        // do-while loop
        System.out.println("\n================");
        int counter = 60;
        do
        {
            System.out.print(counter + " ");
            counter++;
        }
        while (counter < 50);


    }

}
